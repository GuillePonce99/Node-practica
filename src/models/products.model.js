import mongoose from "mongoose";
const Schema = mongoose.Schema;
const collection = "products"

const productSchema = new Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    code: {
        type: String,
        required: true,
        unique: true
    },
    price: {
        type: Number,
        required: true
    },
    stock: {
        type: Number,
        required: true
    },
    category: {
        type: String,
        required: true
    }
});

const ProductModel = mongoose.model(collection, productSchema)
export default ProductModel